package core.bill.comercial.model;

public class ContractDashBoardModel {

private Long count;
private Long contId;
private Double firstSum;
private Double secondsum;
private String name;

public Long getCount() {
	return count;
}
public void setCount(Long count) {
	this.count = count;
}
public Long getContId() {
	return contId;
}
public void setContId(Long contId) {
	this.contId = contId;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Double getFirstSum() {
	return firstSum;
}
public void setFirstSum(Double firstSum) {
	this.firstSum = firstSum;
}
public Double getSecondsum() {
	return secondsum;
}
public void setSecondsum(Double secondsum) {
	this.secondsum = secondsum;
}


}
