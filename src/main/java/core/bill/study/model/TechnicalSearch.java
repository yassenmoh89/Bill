package core.bill.study.model;

public class TechnicalSearch {
	
	private String technicalName;
	private int technicalId;
	private String technicalCode;
	private String status ;
	private String phone ;
	
	
	public String getTechnicalName() {
		return technicalName;
	}
	public void setTechnicalName(String technicalName) {
		this.technicalName = technicalName;
	}
	public int getTechnicalId() {
		return technicalId;
	}
	public void setTechnicalId(int technicalId) {
		this.technicalId = technicalId;
	}
	public String getTechnicalCode() {
		return technicalCode;
	}
	public void setTechnicalCode(String technicalCode) {
		this.technicalCode = technicalCode;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}

}
