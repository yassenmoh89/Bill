package core.model.setting;

public class InspectionDecision {
	private int decisionTypeID;
	private String decisiontypeName;
	private String decisionTypeNameEng;
	
	public int getDecisionTypeID() {
		return decisionTypeID;
	}
	public void setDecisionTypeID(int decisionTypeID) {
		this.decisionTypeID = decisionTypeID;
	}
	public String getDecisiontypeName() {
		return decisiontypeName;
	}
	public void setDecisiontypeName(String decisiontypeName) {
		this.decisiontypeName = decisiontypeName;
	}
	public String getDecisionTypeNameEng() {
		return decisionTypeNameEng;
	}
	public void setDecisionTypeNameEng(String decisionTypeNameEng) {
		this.decisionTypeNameEng = decisionTypeNameEng;
	}
	
	
	

}
