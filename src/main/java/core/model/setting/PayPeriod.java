package core.model.setting;

public class PayPeriod {
	private int payPeroidID;
	private String payPeriod;
	private String payPeriodUS;
	
	public int getPayPeroidID() {
		return payPeroidID;
	}
	public String getPayPeriod() {
		return payPeriod;
	}
	public String getPayPeriodUS() {
		return payPeriodUS;
	}
	public void setPayPeroidID(int payPeroidID) {
		this.payPeroidID = payPeroidID;
	}
	public void setPayPeriod(String payPeriod) {
		this.payPeriod = payPeriod;
	}
	public void setPayPeriodUS(String payPeriodUS) {
		this.payPeriodUS = payPeriodUS;
	}
	
	

}
