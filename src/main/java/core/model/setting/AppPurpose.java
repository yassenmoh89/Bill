package core.model.setting;

public class AppPurpose {
	
	private int appPurposeID;
	private String appPurposeNameEs;
	private String appPurposeNameEn;
	public int getAppPurposeID() {
		return appPurposeID;
	}
	public String getAppPurposeNameEs() {
		return appPurposeNameEs;
	}
	public String getAppPurposeNameEn() {
		return appPurposeNameEn;
	}
	public void setAppPurposeID(int appPurposeID) {
		this.appPurposeID = appPurposeID;
	}
	public void setAppPurposeNameEs(String appPurposeNameEs) {
		this.appPurposeNameEs = appPurposeNameEs;
	}
	public void setAppPurposeNameEn(String appPurposeNameEn) {
		this.appPurposeNameEn = appPurposeNameEn;
	}
	
	

}
