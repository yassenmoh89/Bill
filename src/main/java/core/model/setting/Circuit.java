package core.model.setting;

public class Circuit {
	private int circuitID;
	private String localCode;
	private String circuitDate ;
	private String circuitNumber;
	public int getCircuitID() {
		return circuitID;
	}
	public void setCircuitID(int circuitID) {
		this.circuitID = circuitID;
	}
	public String getLocalCode() {
		return localCode;
	}
	public void setLocalCode(String localCode) {
		this.localCode = localCode;
	}
	public String getCircuitDate() {
		return circuitDate;
	}
	public void setCircuitDate(String circuitDate) {
		this.circuitDate = circuitDate;
	}
	public String getCircuitNumber() {
		return circuitNumber;
	}
	public void setCircuitNumber(String circuitNumber) {
		this.circuitNumber = circuitNumber;
	}
	
	

}
