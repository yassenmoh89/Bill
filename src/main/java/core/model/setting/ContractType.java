package core.model.setting;

public class ContractType {
	
	private int contractTypeID;
	private String contractTypeName;
	private String contractTypeNameUS;
	
	
	public int getContractTypeID() {
		return contractTypeID;
	}
	public String getContractTypeName() {
		return contractTypeName;
	}
	public String getContractTypeNameUS() {
		return contractTypeNameUS;
	}
	public void setContractTypeID(int contractTypeID) {
		this.contractTypeID = contractTypeID;
	}
	public void setContractTypeName(String contractTypeName) {
		this.contractTypeName = contractTypeName;
	}
	public void setContractTypeNameUS(String contractTypeNameUS) {
		this.contractTypeNameUS = contractTypeNameUS;
	}
	
	

}
