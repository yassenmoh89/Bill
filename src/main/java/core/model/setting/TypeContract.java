package core.model.setting;

public class TypeContract {

	private int contractTypeID;
	private String contractTypeNameEs;
	private String contractTypeNameEn;
	public int getContractTypeID() {
		return contractTypeID;
	}
	public String getContractTypeNameEs() {
		return contractTypeNameEs;
	}
	public String getContractTypeNameEn() {
		return contractTypeNameEn;
	}
	public void setContractTypeID(int contractTypeID) {
		this.contractTypeID = contractTypeID;
	}
	public void setContractTypeNameEs(String contractTypeNameEs) {
		this.contractTypeNameEs = contractTypeNameEs;
	}
	public void setContractTypeNameEn(String contractTypeNameEn) {
		this.contractTypeNameEn = contractTypeNameEn;
	}
	
	
	
}
