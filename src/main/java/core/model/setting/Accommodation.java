package core.model.setting;

public class Accommodation {
	private int accomID ;
	private String accomName;
	private String accomNameEng;
	
	public int getAccomID() {
		return accomID;
	}
	public void setAccomID(int accomID) {
		this.accomID = accomID;
	}
	public String getAccomName() {
		return accomName;
	}
	public void setAccomName(String accomName) {
		this.accomName = accomName;
	}
	public String getAccomNameEng() {
		return accomNameEng;
	}
	public void setAccomNameEng(String accomNameEng) {
		this.accomNameEng = accomNameEng;
	}
	
	

}
