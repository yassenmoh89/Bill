package core.model.setting;

public class ConnectionType {
	private int connTypeID;
	private String connTypeName;
	private String connTypeNameEng;
	
	public int getConnTypeID() {
		return connTypeID;
	}
	public void setConnTypeID(int connTypeID) {
		this.connTypeID = connTypeID;
	}
	public String getConnTypeName() {
		return connTypeName;
	}
	public void setConnTypeName(String connTypeName) {
		this.connTypeName = connTypeName;
	}
	public String getConnTypeNameEng() {
		return connTypeNameEng;
	}
	public void setConnTypeNameEng(String connTypeNameEng) {
		this.connTypeNameEng = connTypeNameEng;
	}
	
	

}
